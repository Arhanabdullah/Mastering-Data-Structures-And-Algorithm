package JavaBasics;

public class TypeCasting {
    public static void main(String args[]){
        double num = 56.667;
        int num2 = (int) num;
        System.out.println(num2);
        float num3 = 84.6439f;
        int num4 = (int) num3;
        System.out.println(num4);
        System.out.println(num2+num4);
    }
}
